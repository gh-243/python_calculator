"""Trigonometric functions for the calculator."""
import math

def sin(x):
    """
    Calculate the sine of x (in radians).
    
    Args:
        x: Angle in radians
        
    Returns:
        The sine of x
    """
    return math.sin(x)
def cos(x):
    """
    Calculate the cosine of x (in radians).
    
    Args:
        x: Angle in radians
        
    Returns:
        The cosine of x
    """
    return math.cos(x)
